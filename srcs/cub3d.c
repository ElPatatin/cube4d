int	main(int ac, char **av)
{
	t_game	game;

	if (ac != 2)
		return (print_error(ERR_ARGS, SYS_ARGS));
	// open_map(av[1]);
	init_map(&game, simple_map);
	init_player(&game);
	graphics(&game);
	exit_game(&game);
	(void)av;
	return (EXIT_SUCCESS);
}

void	graphics(t_game *game)
{
	t_mlx	vals;
	t_data	data;

	init_vals(&vals, &data);
	new_window(&vals, &data);
	hooks(&vals);
	game_loop(&vals, game);
}

void	game_loop(t_mlx *vals, t_game *game)
{
	int		x;

	while (42)
	{
		x = -1;
		while (++x < WINWIDTH)
		{
			calc_ray_pos_dir(game, x);
			calc_ray_delta_dist(game);
			calc_ray_step_and_side_dist(game);
			perform_dda(game, vals, x);
		}
		mlx_put_image_to_window(vals->ptr, vals->win, vals->data->img, 0, 0);
		mlx_loop(vals->ptr);
	}
}

void	hooks(t_mlx *vals)
{
	mlx_key_hook(vals->win, key_close_on_esc, vals);
	mlx_hook(vals->win, 2, 0, key_print_hook, vals);
	mlx_hook(vals->win, 17, 0, mouse_close_onclick, vals);
}
